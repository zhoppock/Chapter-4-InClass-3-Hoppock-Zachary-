// Program will do a do-while loop on how many numbers the variable "i" is
// Version # 1
#include <iostream>
using namespace std;

int main()
{ 
  int i = 0;
  do
  {
    cout << "i is = " << i << endl;
    i++;
  }
  while (i < 10);
  return 0;
}
